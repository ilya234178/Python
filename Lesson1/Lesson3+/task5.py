for _ in range(3):  # Цикл выполняется 3 раза
    name = input("Введите имя друга: ")  # Запрашиваем имя
    age = int(input(f"Введите возраст {name}: "))  # Запрашиваем возраст

    if age < 18:
        print(f"Доступ запрещен для {name}")  # Если возраст меньше 18
    else:
        print(f"Доступ разрешен для {name}")  # Если возраст 18 или больше
